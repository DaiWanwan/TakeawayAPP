<template>
  <div class="star" :class="starType">
    <span v-for="itemClass in itemClasses" :class="itemClass" class="star-item" track-by="$index"></span>
  </div>
</template>
<script type="text/ecmascript-6">
  const LENGTH = 5;
  const CLS_ON = 'on';
  const CLS_HALF = 'half';
  const CLS_OFF = 'off';
  export default{
    props: {
      size: {
        type: Number
      },
      score: {
        type: Number
      }
    },
    computed: {
      starType() {
        return 'star-' + this.size;
      },
      itemClasses() {
        let result = [];
        let score = Math.floor(this.score * 2) / 2;   //  取0.5的倍数，*2向下取0.5
        let hasDecimal = score % 1 !== 0;  //  是否有0.5，有为1
        let integer = Math.floor(score);   //  取得整数部分
        for (let i = 0; i < integer; i++) {
          result.push(CLS_ON);   //  先填入整颗星
        }
        if (hasDecimal) {
          result.push(CLS_HALF);  //  如果有小数则填入一个半颗星
        }
        while (result.length < LENGTH) {
          result.push(CLS_OFF);   //  剩下的填入无星
        }
        return result;
      }
    }
  };
</script>
